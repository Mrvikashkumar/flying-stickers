// random num generator
const getRandomNum = (range) => Math.floor(Math.random() * range);

// ssticker effect
const stickerEffect = (x, y) => {
  const sticker = document.createElement("span");
  sticker.className = "stickers";
  const size = `${getRandomNum(100)}px`;
  const radius = `${getRandomNum(100)}%`;
  const color = colors[getRandomNum(colors.length)];
  sticker.style.top = y + "px";
  sticker.style.left = x + "px";
  sticker.style.width = size;
  sticker.style.height = size;
  sticker.style.borderRadius = radius;
  sticker.style.backgroundColor = color;
  appBody.append(sticker);

  setTimeout(() => {
    sticker.remove();
  }, 5000);
};

// mousemove event on appBody
appBody.addEventListener("mousemove", (e) => {
  let x = e.offsetX;
  let y = e.offsetY;

  stickerEffect(x, y);
});

